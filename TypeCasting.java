   class TypeCasting {
	   public static void main(String[] args) {
		   double myValue = 10.05;
		   int intValue = (int) myValue;
		   
		System.out.println("Original Double Value: " + myValue);
		System.out.println("Converted Double Value: " + intValue);
	   }
   }


