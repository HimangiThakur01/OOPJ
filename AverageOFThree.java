  class AverageOFThree {
	  public static void main(String[] args) {
		  int FirstValue = 20;
		  int SecondValue = 48;
		  int ThirdValue = 96;
		  double average = ((double) (FirstValue + SecondValue +ThirdValue)) / 3;
		  System.out.println("Average: " + average);
	  }
  }
		  
		  


